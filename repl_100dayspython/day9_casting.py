birth_year = int(input("What year were you born? "))
if 1883 <= birth_year <= 1900:
  print("You're part of the Lost Generation")
elif 1901 <= birth_year <= 1927:
  print("You're part of the Greatest Generation")
elif 1928 <= birth_year <= 1945:
  print("You're part of the Silent Generation")
elif 1946 <= birth_year <= 1964:
  print("You're part of the Baby Boomers Generation")
elif 1965 <= birth_year <= 1980:
  print("You're part of the Generation X Generation")
elif 1981 <= birth_year <= 1996:
  print("You're part of the Millennials Generation")
elif 1997 <= birth_year <= 2012:
  print("You're part of the Generation Z Generation")
elif 2013 <= birth_year <= 2023:
  print("You're part of the Generation Alpha Generation")
